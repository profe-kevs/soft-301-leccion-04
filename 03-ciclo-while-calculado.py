
arreglo_cadena = ["Kevin", "Z", "Y", "X", "W", "V", "U"]

indice = 0

tamaño_arreglo = len(arreglo_cadena)

while indice <= tamaño_arreglo - 1:
    print("Valor de índice: "+ str(indice))
    print("Valor de elemento: "+ str(arreglo_cadena[indice]))
    indice = indice + 1

